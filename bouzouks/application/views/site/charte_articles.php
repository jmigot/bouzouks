	<div class="margin-petit">

		<h3 class="frameborder_gris padding arrondi">Article 1</h3>
		<p>Bouzouks est un jeu soumis aux lois françaises, par conséquent les joueurs sont priés de respecter les lois en vigueur dans ce pays.
		Certains articles rappelleront des lois susceptibles d'être enfreintes en jouant à Bouzouks.</p>

		<h3 class="frameborder_gris padding arrondi">Article 2</h3>
		<p>Les propos des joueurs, que ce soit dans le pseudo, le commentaire, les annonces, les messages ou à tout autre endroit ne doivent pas porter atteinte à l'intégrité de la personne
		(diffamation, dénonciation...) et ne doivent pas être injurieux, pédophile, xénophobe ou raciste.<br><br>

		Si vous rejoignez le tchat bouzouks, vous devez utiliser votre pseudo et en aucun cas vous n'êtes autorisés à usurper le pseudo d'un autre bouzouk.<br><br>
		
		De manière générale, tout comportement impliquant le non respect des bonnes manières et des convenances sera sanctionné.</p>

		<h3 class="frameborder_gris padding arrondi">Article 3</h3>
		<p>Les joueurs sont priés de respecter l'infrastructure technique du site et du serveur. De ce fait, les rechargements de page trop excessifs ou l'utilisation de robots sont formellement interdits.<br><br>
		Toute recherche, exploitation ou divulgation de faille logicielle provoquera le ban immédiat du joueur. Les informations le concernant seront conservées pour mise à disposition
		aux autorités compétentes.<br><br>

		Toute attaque sur notre serveur est une attaque contre l'hébergeur qui aura alors toute latitude pour enquêter et déposer une plainte.
		<a href="http://www.legifrance.gouv.fr/affichCode.do?idSectionTA=LEGISCTA000006149839&amp;cidTexte=LEGITEXT000006070719">Rappel du code pénal en vigueur.</a>
		</p>

		<h3 class="frameborder_gris padding arrondi">Article 4</h3>
		<p>Toute tricherie ou tentative de tricherie sera purement et dûment sanctionnée. La recherche ou l'utilisation de faille de conception du jeu sans l'accord d'un administrateur
		est strictement interdite.<br><br>

		Dans le cas où vous trouveriez par hasard un bug ou une faille facilitant injustement le jeu vis-à-vis des autres joueurs, vous devez en avertir
		immédiatement un administrateur en privé.</p>

		<h3 class="frameborder_gris padding arrondi">Article 5</h3>
		<p>Bouzouks interdit l'anti-jeu. Donc, les comportements pouvant favoriser ou négliger les autres joueurs sont réprimandés. A savoir :</p>
		<ul>
			<li>L'utilisation de plusieurs comptes pour une seule et même personne</li>
			<li>La vente abusive d'objets bradés sur le marché noir à une personne ou à un groupe de personnes</li>
			<li>Le prêt ou le gardiennage de comptes par un autre joueur</li>
			<li>Le spam (envoi massif de messages) pouvant nuire à la qualité du jeu</li>
			<li>Les propagandes par missive/forum/IRC pour les élections, les entreprises, les clans ou autre</li>
		</ul>

		<h3 class="frameborder_gris padding arrondi">Article 6</h3>
		<p>
			Si vous êtes plusieurs comptes sur la même connexion internet, vous risquez d'être considérés comme multicomptes et vous devez impérativement prévenir un administrateur par missive (message privé) en nous donnant les pseudos concernés
			ainsi que par exemple le lien de parenté.<br><br>

			Si vous êtes autorisés à jouer à plusieurs au même endroit, vous ne devez avoir aucune interaction entre vos comptes : pas de ventes au
			marché noir, pas de votes aux élections, pas de dons mendiant, pas d'employé/patron, etc.<br><br>

			<span class="pourpre">Tous les comptes qui n'ont pas prévenu un administrateur sont susceptibles d'être bannis sans préavis.</span>
		</p>
		
		<h3 class="frameborder_gris padding arrondi">Article 7</h3>
		<p>Les modérateurs travaillent bénévolement. Les joueurs sont donc priés de respecter leurs décisions qui ne sont pas contestables et qui n'ont pas à être justifiées.<br><br>
		En cas de conflit avec un modérateur, le joueur peut en discuter en privé avec un administrateur.</p>

		<h3 class="frameborder_gris padding arrondi">Article 8</h3>
		<p>L'utilisation du système <a href="<?= site_url('plus_de_struls') ?>">Plus de struls !</a> étant géré par la société <a href="http://www.allopass.com">Allopass</a>,
		les administrateurs du site déclinent toute responsabilité en cas de perte ou de la saisie d'un code invalide.<br><br>
		
		Aucun remboursement ne sera fait pour une quelconque raison. Il est à noter que l'utilisation de ce systéme n'entraine pas un assouplissement de la présente charte pour le joueur
		concerné.</p>
		
		<h3 class="frameborder_gris padding arrondi">Article 9</h3>
		<p>Tout manquement à la présente charte sera sanctionné selon l'avis des administrateurs et des modérateurs qui se réservent un droit de regard sur les comptes suspects.
		La suppression d'un compte pourra être faite sans préavis.<br><br>

		De manière générale, les administrateurs se réservent un droit de regard complet sur toutes les traces que vous laissez sur le site. Ceci inclut donc les messages privés,
		les informations personnelles, les logs de connexion et toute autre information détenue dans les fichiers du serveur.<br><br>

		<span class="pourpre">Un joueur peut être banni par un administrateur sans la moindre justification et dans ce cas les dépenses faites auprès du service
		<a href="<?= site_url('plus_de_struls') ?>">Plus de struls !</a> ne seront pas remboursées.</span>
		</p>

		<h3 class="frameborder_gris padding arrondi">Article 10</h3>
		<p>La TeamBouzouk se réserve la possibilité de modifier, à tout moment et sans préavis, la présente charte afin de l'adapter aux évolutions du site et/ou de son exploitation.
		L'utilisateur s'engage donc à la consulter régulièrement.</p>

