Après presque 4 ans d'attente c'est maintenant officiel, la bêta jouable de bouzouks.net est enfin disponible !

Pour vous remercier de votre intérêt pour ce jeu nous vous invitons à tester la bêta fermée.Celle-ci n'étant pas encore pleinement fonctionnelle, certaines parties du jeu peuvent ne fonctionner que partiellement. Pas d'inquiétude, tout sera réglé au fur et à mesure de l'avancement de la bêta.

Nous ouvrons le site en accès restreint afin d'avoir des conditions de test idéales. Vous n'avez pas seulement été sélectionnés pour jouer au jeu mais aussi pour le tester et nous informer des différents bugs, erreurs, failles que vous rencontrerez.

Quand vous aurez besoin de signaler un souci sur le site il faudra contacter un administrateur via la boîte à missives (messagerie du jeu) ou en direct par message privé sur le t'chat IRC.
Vous pouvez également envoyer un mail à <?= $this->config->item('email_from') ?>, mais nous préférons toutefois les missives et le t'chat.
Il est interdit de signaler un bug ou une faille dans le forum ou dans un t'chat public. Merci de garder toutes les informations confidentielles.

Comme sur la première version du site, le forum de discussion est lié au site. Donc votre compte créé sur le jeu fonctionnera aussi sur le forum.
Le site n'est pas vraiment une V2 mais plutôt une reprise de la V1, le game-play reste le même à l'exception de quelques modifications déjà mises en place afin d'accueillir les futures vraies nouveautés du jeu ;-)

Pour toute question n'hésitez pas à vous connecter au t'chat IRC où vous pourrez régulièrement croiser la TeamBouzouk.

Merci de votre participation à l'avancement du site et amusez vous bien ! ;-)

