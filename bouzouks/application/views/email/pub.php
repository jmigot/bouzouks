Après des mois acharnés de développement, la TeamBouzouk est fière de te présenter les nouveautés de bouzouks.net qui auraient pu t'échapper ces dernières semaines :

- les clans : c'est officiel, chaque bouzouk peut désormais créer/rejoindre plusieurs clans (syndicat, parti politique ou organisation) sur le site et lancer des actions
- les actions des clans : voler et revendre un objet shop, lancer une grêve, truquer les élections, renverser le maire, envoyer son patron à l'asile, bloquer un shop, voler les struls des mendiants, être informé d'une taxe et bien d'autres encore (35 actions à ce jour) !
- retour des stats force/charisme/intelligence qui servent de points d'action pour les clans
- bonus de production pour les employés et les patrons selon la forme de ton bouzouk (faim/santé/stress) et selon ses stats (force/charisme/intelligence), tu peux gagner plus de struls au boulot !
- le Plouk : jeu de cartes bouzouks en temps réel à deux joueurs avec pari d'objets possible
- système de notifications sur le jeu pour être averti en temps réel d'un évènement te concernant (emploi, achats/ventes, plouk, lancement d'action par un clan, réception d'une missive, etc.)
- améliorations diverses dans le jeu : numéros tirés au Lohtoh disponibles, nouveaux objets dans les shops, nouveaux objets rares/très rares, mini-CV sur les profils, etc. 

